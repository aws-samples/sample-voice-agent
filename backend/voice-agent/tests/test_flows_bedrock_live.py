# Test: Tool swap mid-conversation (simulating node transition)
# =============================================================================


class TestToolSwapMidConversation:
    """Verify that Bedrock handles tool set changes between turns,
    simulating what happens during a FlowManager node transition."""

    def _build_bedrock_tool_config(self, functions):
        """Build Bedrock toolConfig from direct functions."""
        tools = []
        for func in functions:
            wrapper = FlowsDirectFunctionWrapper(function=func)
            schema = wrapper.to_function_schema()
            tool_spec = {
                "toolSpec": {
                    "name": schema.name,
                    "description": schema.description,
                    "inputSchema": {
                        "json": {
                            "type": "object",
                            "properties": schema.properties,
                            "required": schema.required,
                        }
                    },
                }
            }
            tools.append(tool_spec)
        return {"tools": tools}

    def test_tool_set_changes_between_turns(self):
        """Bedrock should accept a different tool set on the second turn.

        This simulates: Turn 1 (reception) has transfer tool only.
        Turn 2 (specialist) has KB search + transfer tools.
        """
        client = get_bedrock_client()

        # Turn 1: Reception with transfer only
        reception_tools = self._build_bedrock_tool_config([transfer])

        response1 = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [{"text": "I need computer help."}],
                }
            ],
            system=[{"text": "You are a receptionist. Route the caller."}],
            toolConfig=reception_tools,
        )
        assert response1["ResponseMetadata"]["HTTPStatusCode"] == 200

        # Turn 2: Specialist with KB search + transfer (different tool set)
        specialist_tools = self._build_bedrock_tool_config(
            [search_knowledge_base, transfer]
        )

        response2 = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {"text": "My WiFi keeps dropping. Can you look up any guides?"}
                    ],
                }
            ],
            system=[
                {
                    "text": (
                        "You are a computer support specialist. "
                        "Use search_knowledge_base to find troubleshooting guides."
                    )
                }
            ],
            toolConfig=specialist_tools,
        )

        assert response2["ResponseMetadata"]["HTTPStatusCode"] == 200
        # The specialist should try to search the KB
        assert response2["stopReason"] in ("tool_use", "end_turn")
