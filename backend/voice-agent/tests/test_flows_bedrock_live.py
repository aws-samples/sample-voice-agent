# Test: Direct functions produce valid Bedrock tool schemas
# =============================================================================


class TestDirectFunctionToolSchemas:
    """Verify that direct functions produce tool schemas Bedrock accepts."""

    def test_transfer_function_schema_is_valid(self):
        """Transfer function should produce a schema with target and reason params."""
        wrapper = FlowsDirectFunctionWrapper(function=transfer)
        schema = wrapper.to_function_schema()

        assert schema.name == "transfer"
        assert "target" in schema.properties
        assert "reason" in schema.properties
        assert schema.properties["target"]["type"] == "string"
        assert schema.properties["reason"]["type"] == "string"
        assert "target" in schema.required
        assert "reason" in schema.required

    def test_search_kb_function_schema_is_valid(self):
        """KB search function should produce a schema with query param."""
        wrapper = FlowsDirectFunctionWrapper(function=search_knowledge_base)
        schema = wrapper.to_function_schema()

        assert schema.name == "search_knowledge_base"
        assert "query" in schema.properties
        assert schema.properties["query"]["type"] == "string"

    def test_tools_schema_serializes_for_bedrock(self):
        """ToolsSchema containing direct functions should be serializable."""
        transfer_wrapper = FlowsDirectFunctionWrapper(function=transfer)
        kb_wrapper = FlowsDirectFunctionWrapper(function=search_knowledge_base)

        tools_schema = ToolsSchema(
            standard_tools=[
                transfer_wrapper.to_function_schema(),
                kb_wrapper.to_function_schema(),
            ]
        )

        # Verify ToolsSchema is not empty
        assert tools_schema is not None


