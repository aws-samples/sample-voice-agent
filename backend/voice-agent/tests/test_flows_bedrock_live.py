# Test: Bedrock Converse API accepts FlowManager-style tool definitions
# =============================================================================


class TestBedrockAcceptsFlowsToolSchemas:
    """Verify that Bedrock's Converse API accepts the tool schemas
    generated by Flows' direct functions via UniversalLLMAdapter."""

    def _build_bedrock_tool_config(self, functions):
        """Convert direct functions to Bedrock toolConfig format.

        This replicates what happens inside the pipeline: FlowManager creates
        ToolsSchema -> Pipecat's Bedrock adapter converts to native format.
        """
        tools = []
        for func in functions:
            wrapper = FlowsDirectFunctionWrapper(function=func)
            schema = wrapper.to_function_schema()
            tool_spec = {
                "toolSpec": {
                    "name": schema.name,
                    "description": schema.description,
                    "inputSchema": {
                        "json": {
                            "type": "object",
                            "properties": schema.properties,
                            "required": schema.required,
                        }
                    },
                }
            }
            tools.append(tool_spec)
        return {"tools": tools}

    def test_bedrock_accepts_transfer_tool(self):
        """Bedrock should accept a Converse call with the transfer tool schema."""
        client = get_bedrock_client()
        tool_config = self._build_bedrock_tool_config([transfer])

        response = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [{"text": "My laptop won't connect to WiFi."}],
                }
            ],
            system=[
                {
                    "text": (
                        "You are a receptionist. Identify the caller's intent and "
                        "transfer them to the appropriate specialist using the "
                        "transfer function."
                    )
                }
            ],
            toolConfig=tool_config,
        )

        assert response["ResponseMetadata"]["HTTPStatusCode"] == 200
        # The model should either call transfer or provide text
        stop_reason = response["stopReason"]
        assert stop_reason in ("tool_use", "end_turn")

    def test_bedrock_calls_transfer_for_routing(self):
        """Bedrock should call the transfer tool when given a clear routing intent."""
        client = get_bedrock_client()
        tool_config = self._build_bedrock_tool_config([transfer])

        response = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "text": "I need help with my WiFi connection. It keeps dropping."
                        }
                    ],
                }
            ],
            system=[
                {
                    "text": (
                        "You are a receptionist. Your ONLY job is to route callers "
                        "to specialists. When the caller describes a technical issue, "
                        "immediately use the transfer function with target='computer_support'. "
                        "Do NOT try to help them yourself."
                    )
                }
            ],
            toolConfig=tool_config,
        )

        assert response["stopReason"] == "tool_use"

        # Extract the tool use block
        output_message = response["output"]["message"]
        tool_uses = [
            block["toolUse"]
            for block in output_message["content"]
            if "toolUse" in block
        ]
        assert len(tool_uses) >= 1
        assert tool_uses[0]["name"] == "transfer"
        assert tool_uses[0]["input"]["target"] == "computer_support"

    def test_bedrock_accepts_specialist_tool_set(self):
        """Bedrock should accept a specialist's tool set (KB search + transfer)."""
        client = get_bedrock_client()
        tool_config = self._build_bedrock_tool_config([search_knowledge_base, transfer])

        response = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {"text": "My WiFi keeps disconnecting every 10 minutes."}
                    ],
                }
            ],
            system=[
                {
                    "text": (
                        "You are a computer support specialist. Help the caller "
                        "troubleshoot their issue. Use search_knowledge_base to "
                        "find relevant troubleshooting guides. Use transfer if "
                        "you need to route to another specialist."
                    )
                }
            ],
            toolConfig=tool_config,
        )

        assert response["ResponseMetadata"]["HTTPStatusCode"] == 200
        assert response["stopReason"] in ("tool_use", "end_turn")


