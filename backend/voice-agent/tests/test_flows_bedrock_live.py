# Direct function definitions (same as will be used in Phase 1)
# =============================================================================


async def transfer(flow_manager, target: str, reason: str) -> tuple[FlowResult, None]:
    """Transfer the caller to a different specialist or back to reception.

    Args:
        target: The specialist to transfer to. One of: computer_support,
                billing, appointment, reception.
        reason: Brief explanation of why the transfer is needed.
    """
    return {"transferred_to": target, "reason": reason}, None


async def search_knowledge_base(flow_manager, query: str) -> tuple[FlowResult, None]:
    """Search the knowledge base for troubleshooting guides and documentation.

    Args:
        query: Natural language search query describing the issue.
    """
    return {
        "found": True,
        "results": [{"content": "Try restarting your router."}],
    }, None


