# =============================================================================


class TestResetWithSummarySummaryGeneration:
    """Verify that Bedrock generates coherent summaries for context resets."""

    def test_summary_generation_via_converse(self):
        """Bedrock should produce a coherent summary of a conversation."""
        client = get_bedrock_client()

        # Simulate the conversation history that RESET_WITH_SUMMARY would summarize
        conversation_history = [
            {
                "role": "user",
                "content": [{"text": "Hi, my laptop can't connect to WiFi."}],
            },
            {
                "role": "assistant",
                "content": [
                    {
                        "text": "I can help with that. Let me transfer you to our tech support team."
                    }
                ],
            },
        ]

        summary_prompt = (
            "Summarize the caller's technical issue and any details "
            "they provided. Be concise -- one or two sentences."
        )

        response = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "text": f"Conversation history: {json.dumps(conversation_history)}\n\n{summary_prompt}"
                        }
                    ],
                }
            ],
            system=[{"text": summary_prompt}],
        )

        assert response["ResponseMetadata"]["HTTPStatusCode"] == 200
        assert response["stopReason"] == "end_turn"

        # Extract summary text
        output_content = response["output"]["message"]["content"]
        summary_text = "".join(
            block["text"] for block in output_content if "text" in block
        )

        assert len(summary_text) > 10
        # Summary should mention WiFi or laptop (the core issue)
        summary_lower = summary_text.lower()
        assert (
            "wifi" in summary_lower
            or "laptop" in summary_lower
            or "connect" in summary_lower
        )

    def test_summary_generation_with_multi_turn_history(self):
        """Bedrock should summarize a longer multi-turn conversation."""
        client = get_bedrock_client()

        conversation = [
            {
                "role": "user",
                "content": [
                    {"text": "I have a billing question about my last invoice."}
                ],
            },
            {
                "role": "assistant",
                "content": [
                    {
                        "text": "I'd be happy to help with billing. Can you tell me your account number?"
                    }
                ],
            },
            {
                "role": "user",
                "content": [
                    {"text": "It's 4532. I was charged twice for the same service."}
                ],
            },
            {
                "role": "assistant",
                "content": [
                    {
                        "text": "I see the duplicate charge. Let me look into that for you."
                    }
                ],
            },
        ]

        summary_prompt = (
            "Summarize the caller's billing concern, any account "
            "information already verified, and the current status. Be concise."
        )

        response = client.converse(
            modelId=MODEL_ID,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "text": f"Conversation history: {json.dumps(conversation)}\n\n{summary_prompt}"
                        }
                    ],
                }
            ],
            system=[{"text": summary_prompt}],
        )

        summary_text = "".join(
            block["text"]
            for block in response["output"]["message"]["content"]
            if "text" in block
        )

        summary_lower = summary_text.lower()
        # Should mention billing/charge/duplicate
        assert (
            "billing" in summary_lower
            or "charge" in summary_lower
            or "duplicate" in summary_lower
            or "invoice" in summary_lower
        )


